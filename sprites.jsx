function PixelIcon({ name, size = 16, color, palette }) {
  const data = ICONS[name];
  if (!data) return null;
  const pal = palette || (color ? { "1": color, "2": color, "3": color } : PAL_INK);
  return (
    <PixelSprite
      data={data}
      size={size}
      palette={pal}
      title={name}
    />
  );
}

