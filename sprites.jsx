// sprites.jsx — 16x16 (and 32x32) pixel-art sprites rendered as SVG <rect>
// grids. Inline SVG is the right tool here: each "pixel" is a 1x1 rect, the
// viewBox handles scaling, and shape-rendering="crispEdges" guarantees no
// anti-aliasing. Themable — pass a palette mapping char→CSS color (the
// values can be CSS variables, so sprites reskin per theme automatically).
//
// Sprite-data convention: each sprite is an array of strings, one per row.
// "." or " " → transparent. Any other char → looked up in the palette.

// ── core component ──────────────────────────────────────────────────────────
function PixelSprite({ data, palette, size = 32, w, h, className, style, title }) {
  const rows = data.length;
  const cols = data[0].length;
  const W = w || cols, H = h || rows;
  return (
    <svg
      className={className}
      width={size}
      height={size * (rows / cols)}
      viewBox={`0 0 ${W} ${H}`}
      shapeRendering="crispEdges"
      style={{ display: "inline-block", verticalAlign: "middle", ...style }}
      role={title ? "img" : "presentation"}
      aria-label={title}
    >
      {data.flatMap((row, y) =>
        [...row].map((c, x) => {
          if (c === "." || c === " ") return null;
          const fill = palette[c] || c;
          return <rect key={`${x}-${y}`} x={x} y={y} width="1" height="1" fill={fill} />;
        }),
      )}
    </svg>
  );
}

